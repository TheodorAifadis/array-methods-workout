// fill är en metod som ändrar element i en array till ett värde
// metoden byter ut följande element till ett annat ord
const cars = ['lamborghini', 'bmw', 'audi', 'volkswagen', 'porsche', 'saab', 'renault', 'ferrari']
console.log(cars.fill('VOLVO', 1, 7)) // det här borde byta ut element 1-7 till VOLVO